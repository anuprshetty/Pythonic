# Polymorphism:
# - Poly --> Many
# - Morph --> Form
# - Objects have multiple forms.
# - Polymorphism is defined as an object that can take on various forms.

# - The kind of polymorphism the object undergoes depends on when the object takes its form and what part of the object is transforming.
# -- When the object transforms:
# 1. Compile-time
# 2. Dynamic
# -- What does the transforming:
# 1. Method
# 2. Object

# - “In programming languages and type theory, polymorphism is the provision of a single interface to entities of different types, or the use of a single symbol to represent multiple different types.”

# Use cases: Polymorphism is used in
# - Loose Coupling
# - Dependency injection
# - Interface

# 4 types of polymorphism:
# 1. Subtype polymorphism (Runtime)
# 2. Parametric polymorphism (Overloading)
# 3. Ad hoc polymorphism (Compile-time)
# 4. Coercion polymorphism (Casting)

# 4 ways of implementing polymorphism:
# 1. Duck Typing
# 2. Operator Overloading
# 3. Method Overloading
# 4. Method Overriding
